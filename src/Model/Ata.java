public class Ata {
    
    private int id;
    private int idIgreja;
    private String data;
    private String titulo;
    private String descricao;
    private String pastor;
    private String secretario;
    private String votos;

    /**
     * @return the id
     */
    public int getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(int id) {
        this.id = id;
    }

    /**
     * @return the data
     */
    public String getData() {
        return data;
    }

    /**
     * @param data the data to set
     */
    public void setData(String data) {
        this.data = data;
    }

    /**
     * @return the titulo
     */
    public String getTitulo() {
        return titulo;
    }

    /**
     * @param titulo the titulo to set
     */
    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    /**
     * @return the descricao
     */
    public String getDescricao() {
        return descricao;
    }

    /**
     * @param descricao the descricao to set
     */
    public void setDescricao(String descricao) {
        this.descricao = descricao;
    }

    /**
     * @return the pastor
     */
    public String getPastor() {
        return pastor;
    }

    /**
     * @param pastor the pastor to set
     */
    public void setPastor(String pastor) {
        this.pastor = pastor;
    }

    /**
     * @return the secretario
     */
    public String getSecretario() {
        return secretario;
    }

    /**
     * @param secretario the secretario to set
     */
    public void setSecretario(String secretario) {
        this.secretario = secretario;
    }

    /**
     * @return the votos
     */
    public String getVotos() {
        return votos;
    }

    /**
     * @param votos the votos to set
     */
    public void setVotos(String votos) {
        this.votos = votos;
    }

    /**
     * @return the idIgreja
     */
    public int getIdIgreja() {
        return idIgreja;
    }

    /**
     * @param idIgreja the idIgreja to set
     */
    public void setIdIgreja(int idIgreja) {
        this.idIgreja = idIgreja;
    }
    
    
}
